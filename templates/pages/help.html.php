    <h1 class="title" style="text-align: left;">Anleitung</h1>
    <h2>Einloggen</h2>
    <p>
        Auf <a class="helpLink" href="/">dieser</a> Seite können Sie sich einloggen. Dafür ist nur Ihre E-Mail-Adresse
        gefragt. Damit es aber funktioniert,
        muss die Adresse im System hinterlegt sein.
        <br>
        Wenn Sie sich nicht einloggen können, geben Sie uns bitte Bescheid und wir erstellen Ihnen einen Zugang. Dies
        wird sehr
        wahrscheinlich beim ersten Versuch vorkommen.
        <br>
        Nach dem erfolgreichen Login können Sie alle Bestellartikel sehen. Sie sind so lange eingeloggt, bis der Browser
        geschlossen wird.
        <br>
    </p>
    <h2>Bestellung</h2>
    <p>
        Im Bestellfenster sind alle verfügbaren Artikel und der kg-Preis. Zum Bestellen müssen folgende Felder
        ausgefüllt sein.
    </p>
    <h3>Anzahl Päckchen</h3>
    <p>
        In diesem Feld können Sie eingeben, wie viele Päckchen von dem Artikel Sie gerne hätten.
        Es wird eine Zahl zwischen 1 und 15 erwartet.
    </p>
    <img class="helpImg" src="images/anzahl_paeckchen.png" alt="">
    <h3>Gewicht pro Päckchen oder Anzahl Stücke</h3>
    <p>
        Es gibt hier zwei Möglichkeiten. Einige Artikel können mit einer Stückzahl oder mit dem Gewicht bestellt werden.
        Dort steht etwas wie <b>g. / Stk. à ca. X g.</b> In dem Textfeld können Sie angeben, wie viele Stücke
        Sie <b>pro Päckchen</b> wollen. In dem Bild unten ist ein Beispiel. Eine Gewichtangabe ist auch möglich.<br>
        Die andere Möglichkeit ist, dass nur <b>g.</b> steht. Das heisst, dass das gewünschte Gewicht eingegeben
        werden muss. Die Einheit der Gewichteingabe ist Gramm.
    </p>
    <img class="helpImg" src="images/gewicht_pro_paeckchen.png" alt="">
    <h3>Besonderheiten</h3>
    <p>
        Falls Sie etwas anderes oder einfach einen Kommentar hinterlassen möchten z. B., falls Sie mehr möchten
        als verfügbar ist können Sie das in diesem Feld festhalten und wenn übrig bleibt, könnten wir es Ihnen evtl.
        reservieren.
    </p>
    <img class="helpImg" src="images/besonderheiten.png" alt="">
    <h3>Gesamtgewicht(g)</h3>
    <p>
        Hier wird das Gesamtgewicht pro Artikel dargestellt. Wenn Sie jetzt zum Beispiel 3 Päckchen mit 3 Stücke pro
        Päckchen eingeben und das Stückgewicht 150g. ist, ist das Resultat 3 &times; 3 &times; 150g. was 1350g. gibt.
    </p>
    <img class="helpImg" src="images/gesamtgewicht.png" alt="">
    <h3>Bestellung abgeben</h3>
    <p>
        Wenn alles ausgefüllt ist, können Sie auf den Knopf <b>Bestellen</b> drücken.
    </p>
    <h3>Für ein anderes Datum Bestellen</h3>
    <p>
        Es gibt die Möglichkeit für andere Daten voraus zu bestellen. Z. B., wenn Sie sicher ein bestimmtes Fleisch zu
        Weihnachten möchten. Im Bestellfenster gibt es einen Knopf oben rechts, welcher alle Daten anzeigt.
        Es ist möglich, dass Sie aber ein Datum anklicken und keine Bestellartikel zur Verfügung stehen.
        Das heisst, dass Nicolas noch nicht die Artikel festgelegt hat aber das Datum schon eingegeben.
    </p>

    <h3>Verfügbares Gewicht + Bestellgewicht</h3>
    <p>
        Die Angaben sind <b>eine Schätzung</b> des verfügbaren Gewichts. Es variiert immer je nach Gewicht des Rinds.
        Daher kann es sein, dass es einmal mehr gibt als geschrieben aber auch weniger.
        <b>Wenn das verfügbare Gewicht tief ist (es wird Orange) ist die Bestellung nicht garantiert. </b>
        <br>
        Die Bestellungen werden so gut wie möglich vorbereitet <b>im Bereich des Möglichen</b>. Wenn in der Bestellung
        z. B. 1 Päckchen mit 10 Stücke Cotelettes eingegeben wird, ist es nicht möglich und es wird angepasst in
        vielleicht 2 oder 3 Päckchen.

    </p>
    <h3>Zusätzliche Infos</h3>
    <p>
        Wenn Sie Ihre Bestellung schon gemacht haben und später entscheiden etwas zu ändern, können Sie sich
        einfach wieder einloggen und Ihre Änderungen machen. Anschliessend auf "Bestellen" drücken und Ihre alte
        Bestellung
        wird überschrieben.
        <br>
        Bitte Bestellungen bis eine Woche vor dem Verkaufsdatum abgeben. <b>Nachher werden Bestellungen nicht mehr
            aufgenommen.</b>
    </p>
